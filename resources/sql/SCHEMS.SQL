DROP TABLE tblScheme;

CREATE TABLE tblScheme(
       COMID INT,
       SCHEMEID INT not null PRIMARY KEY AUTO_INCREMENT ,
       BRANCHID INT,
       PRODUCTID INT,
       INTERESTID INT,
       INTERESTCODE CHAR(3),
       MCARTAGEID INT,
       CODE VARCHAR(3),
       DESCRIPTION VARCHAR(60),
       ISACTIVE SMALLINT unsigned,
       PERIOD SMALLINT,
       RECSTATUS SMALLINT unsigned,
       LASTUPDATE DATETIME,
       LASTUPTIME MEDIUMTEXT,
       LASTUPUSERID INT,
       VERSIONID INT,
       ORIGINALID INT      
);

   