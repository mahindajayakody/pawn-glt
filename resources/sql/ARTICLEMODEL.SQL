	RECSTATUS SMALLINT,
	LASTUPDATE DATETIME,
	LASTUPTIME LONG,
	LASTUPUSERID INT,
	ORIGINALID INT,
	VERSIONID INT,
	COMID INT,
	BRANCHID INT
);