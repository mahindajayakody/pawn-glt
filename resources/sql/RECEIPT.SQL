DROP TABLE tblreceipt;
CREATE TABLE tblreceipt(
   RCPID INT PRIMARY KEY AUTO_INCREMENT,
   COMID INT,
   BRANCHID INT,
   PRODUCTID INT,
   TICKETID INT,
   RCPNO VARCHAR(12),
   PAWNERID INT,
   DESCRIPTION VARCHAR(200),
   RCPAMOUNT DECIMAL(18,2),
   RCPDATE DATETIME,
   RCPTYPE SMALLINT unsigned,
   PRINTNO INT,
   PRINTDATE DATETIME,
   CHEQUENO VARCHAR(20),
   CHEQUEDATE DATETIME,
   STATUS INT,
   LASTUPDATE VARCHAR(45),
   VERSIONID INT,
   LASTUPTIME MEDIUMTEXT,
   ORIGINALID INT unsigned,
   LASTUPUSERID INT,
   RPCENTUSER VARCHAR(20),
   CANCELUSERID INT,
   CANCELDATE DATETIME
);

