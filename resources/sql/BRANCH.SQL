DROP TABLE tblBranch;

CREATE TABLE tblBranch(
	BRANCHID INT PRIMARY KEY AUTO_INCREMENT,
	COMID INT,
	CODE CHAR(3),
	ADDLINE1 VARCHAR(40),
	ADDLINE2 VARCHAR(40),
	ADDLINE3 VARCHAR(40),
	ADDLINE4 VARCHAR(40),
	TPNO VARCHAR(15),
	FAXNO VARCHAR(15),
	TAXNO VARCHAR(20),
	ISDEFAULT SMALLINT,
	RCPACC VARCHAR(20),
	PMTACC VARCHAR(20),	
	DATEINSTALL DATETIME,
  	FUNDAVAILABLE DECIMAL,
  	FUNDLIMIT DECIMAL,
	RECSTATUS SMALLINT,
	LASTUPDATE DATETIME,
	LASTUPTIME LONG,
	LASTUPUSERID INT,
	ORIGINALID INT,
	VERSIONID INT,
	SYSDATE INT,
	BRANCHNAME VARCHAR(50)
);

