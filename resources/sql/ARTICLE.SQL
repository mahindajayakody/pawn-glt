DROP TABLE tblArticle;

CREATE TABLE tblArticle(
	ARTICALID INT PRIMARY KEY AUTO_INCREMENT,
	CODE CHAR(3),
	DESCRIPTION VARCHAR(50),
	PRODUCTID INT,
	ARTICALMODELID INT,
	ISACTIVE SMALLINT,
	RECSTATUS SMALLINT,
	LASTUPDATE DATETIME,
	LASTUPTIME LONG,
	LASTUPUSERID INT,
	ORIGINALID INT,
	VERSIONID INT,
	COMID INT,
	BRANCHID INT
);