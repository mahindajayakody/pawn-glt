DROP TABLE tblDueTo;

CREATE TABLE tblDueTo(
	DUETOID INT PRIMARY KEY ,
	PRODUCTID INT,
	TICKETID INT,
	DUETYPEID INT,
	PWNID INT,
	DUEAMOUNT DECIMAL(15,2),
	PAIDAMOUNT DECIMAL(15,2),
	BALAMOUNT DECIMAL(15,2),
	LASTUPDATE DATETIME,
	LASTUPTIME LONG,
	LASTUPUSERID INT,	
	VERSIONID INT,
	COMID INT,
	BRANCHID INT
);

