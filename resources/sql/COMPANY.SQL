DROP TABLE tblUserCompany;

CREATE TABLE tblUserCompany(
	COMID INT PRIMARY KEY AUTO_INCREMENT,
	CODE CHAR(3),
	COMPNAME VARCHAR(50),
	ADDLINE1 VARCHAR(40),
	ADDLINE2 VARCHAR(40),
	ADDLINE3 VARCHAR(40),
	ADDLINE4 VARCHAR(40),
	TPNO VARCHAR(15),
	FAXNO VARCHAR(15),
	TAXNO VARCHAR(20),
	DATEINSTALL DATETIME,
	FBIGDATE DATETIME,
	FENDDATE DATETIME,	
	RECSTATUS SMALLINT,
	LASTUPDATE DATETIME,
	LASTUPTIME LONG,
	LASTUPUSERID INT,
	ORIGINALID INT,
	VERSIONID INT,
	AUTHMODE SMALLINT
);